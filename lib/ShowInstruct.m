                                    'guider tout au long de l''epreuve.\n\n\n\nAppuyez sur une', ...
                                    ' touche pour continuer >>> ']);
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['INSTRUCTIONS GENERALES :\n\nPour les prochaines 28 ', ...
                                    'minutes, vous devrez realiser (ordre indefini):\n\n\n', ...
                                    '1 tâche d''effort mental\n1 tâche d''écoute d''un bruit ',...
                                    'fort\n2 periodes de repos\n\n\nAu moment venu, des ',...
                                    'instructions seront diffusees a l''ecran pour\nvous ', ...
                                    'guider tout au long de l''epreuve.\n\n\n\nAppuyez sur une', ...
                                    ' touche pour continuer >>> ']);
            end

    end
        
end
