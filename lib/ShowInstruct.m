                                    'plus immobile possible.\n(Les mouvements et la parole ', ...
                                    'genent les enregistrements)\n\nSi vous avez un probleme, ', ...
                                    'levez la main pour\ninteragir avec l''experimentateur/rice', ...
                                    '\n\n\n\nAppuyez sur "SPACE" pour continuer >>>']);
            end

        case '5'
             if strcmpi(lang, 'fr')
                Instruct = sprintf(['QUESTIONS :\n\nAvant de commencer, avez-vous des ', ...
                                    'questions?\nSi oui, levez la main.\n\n\n\nAppuyez sur ', ...
                                    '"SPACE" pour passer à l''entrainement\nde la tache ', ...
                                    'mentale (duree : 30 secondes) >>> ']);
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['QUESTIONS :\n\nAvant de commencer, avez-vous des ', ...
                                    'questions?\nSi oui, levez la main.\n\n\n\nAppuyez sur ', ...
                                    '"SPACE" pour passer à l''entrainement\nde la tache ', ...
                                    'mentale (duree : 30 secondes) >>> ']);
            end

        case '6'
            if strcmpi(lang, 'fr')
                Instruct = sprintf(['INSTRUCTIONS :\n\nReposez-vous pendant minutes.\nVous ', ...
                                    'pouvez fermer les yeux si vous le souhaitez.\n\n Un ', ...
                                    'signal musical pour avertira que la\nperiode est finie et', ...
                                    ' des instructions\nseront a nouveau presentees a l''écran.']);
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['INSTRUCTIONS :\n\nReposez-vous pendant minutes.\nVous ', ...
                                    'pouvez fermer les yeux si vous le souhaitez.\n\n Un ', ...
                                    'signal musical pour avertira que la\nperiode est finie et', ...
                                    ' des instructions\nseront a nouveau presentees a l''écran.']);
            end

        case '7'
            if strcmpi(lang, 'fr')
                Instruct = '======================== PERIODE DE REPOS ========================';
            elseif strcmpi(lang, 'en')
                Instruct = '======================== REST PERIOD ========================';                
            end

        case '8'
            if strcmpi(lang, 'fr')
                Instruct = ['===================== DIFFUSION DU BRUIT ====================='];
            elseif strcmpi(lang, 'en')
                Instruct = ['===================== BROADCASTING NOISE ====================='];
            end

         case '10'
            if strcmpi(lang, 'fr')
                Instruct = sprintf(['INSTRUCTIONS:\n\nUn bruit fort va petre diffusé via les ', ...
                                    'haut-parleurs pendant quelques minutes.']);
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['INSTRUCTIONS:\n\nUn bruit fort va être diffusé via les ', ...
                                    'haut-parleurs pendant quelques minutes.']);
            end
        case '9'
            if strcmpi(lang, 'fr')
                Instruct = sprintf(['===================== EPREUVE TERMINEE %s\n\n\nMerci pour', ...
                                    ' votre participation !'], '=====================');
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['===================== TASK COMPLETED %s\n\n\nThank you ', ...
                                    'for participating !'], '=====================');
            end
        
        case '11'
            if strcmpi(lang, 'fr')
