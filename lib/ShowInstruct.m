function Instruct = ShowInstruct(lang, instNb, startCount, subtract, trialTout)
    
    % TODO: Validate text of long instruction + translate them

    instNb = num2str(instNb); % not to confound with 1/true or 0/false
       
%     ['\bf ', str{1}, ' \rm']
    switch instNb
        case '1'
            if strcmpi(lang, 'fr')
                Instruct = '======================== EPREUVE MENTALE ========================';
            elseif strcmpi(lang, 'en')
                Instruct = '======================== MENTAL TASK ========================';
            end

        case 'C'
            if strcmpi(lang, 'fr')
                Instruct = '======================== ENTRAINEMENT ========================';
            elseif strcmpi(lang, 'en')
                Instruct = '======================== TRAINING ========================';
            end

        case '2'
            if strcmpi(lang, 'fr')
                Instruct = sprintf(['INSTRUCTIONS CALCUL MENTAL:\n\nVous allez effectuer une ' ...
                                    'tache de calcul mental.\nPrenez le chiffre %d et ' ...
                                    'soustrayez-y %.d. Entrez le nombre obtenu à l''aide du ', ...
                                    'clavier, puis appuyez sur "ENTRER". Continuez de soustraire', ...
                                    ' %.d du nombre obtenu précédemment encore et encore.\nLes', ...
                                    ' résultats validés ne seront pas réaffichés après chaque ', ...
                                    '"ENTRER". \n\n\n À chaque fois, vous n''aurez que %.1f secondes pour' ...
                                    ' repondre.\n\n\nIl est donc important que vous soyez ', ...
                                    'rapide et precis, puisque\ns''il y a une erreur, vous ', ...
                                    'devrez recommencer au début.\n\n\n\nLa tâche va bientot ', ...
                                    'commencer...'], startCount, subtract, subtract, trialTout, ...
                                    startCount);
%                 Instruct = sprintf(['INSTRUCTIONS CALCUL MENTAL:\n\nVous allez effectuer une ' ...
%                                     'tache de calcul mental.\nPrenez le chiffre %d et ' ...
%                                     'soustrayez %.d, puis soustrayez encore %.d, etc.\nA ' ...
%                                     'chaque fois, vous n''aurez que %.1f secondes pour ' ...
%                                     'repondre.\n\n\nIl est donc important que vous soyez ' ...
%                                     'rapide et precis, puisque\ns''il y a une erreur, vous ' ...
%                                     'devrez recommencer a %d.\n\n\n\nLa tache va bientot' ...
%                                     ' commencer...'], startCount, subtract, subtract, ...
%                                     trialTout, startCount);
%             elseif strcmpi(lang, 'en')
%                 Instruct = sprintf(['INSTRUCTIONS CALCUL MENTAL:\n\nVous allez effectuer une ' ...
%                                     'tache de calcul mental.\nPrenez le chiffre %d et ' ...
%                                     'soustrayez %.d, puis soustrayez encore %.d, etc.\nA ' ...
%                                     'chaque fois, vous n''aurez que %.1f secondes pour ' ...
%                                     'repondre.\n\n\nIl est donc important que vous soyez ' ...
%                                     'rapide et precis, puisque\ns''il y a une erreur, vous ' ...
%                                     'devrez recommencer a %d.\n\n\n\nLa tache va bientot' ...
%                                     ' commencer...'], startCount, subtract, subtract, ...
%                                     trialTout, startCount);
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['INSTRUCTIONS CALCUL MENTAL:\n\nVous allez effectuer une ' ...
                                    'tache de calcul mental.\nPrenez le chiffre %d et ' ...
                                    'soustrayez %.d, puis soustrayez encore %.d, etc.\nA ' ...
                                    'chaque fois, vous n''aurez que %.1f secondes pour ' ...
                                    'repondre.\n\n\nIl est donc important que vous soyez ' ...
                                    'rapide et precis, puisque\ns''il y a une erreur, vous ' ...
                                    'devrez recommencer a %d.\n\n\n\nLa tache va bientot' ...
                                    ' commencer...'], startCount, subtract, subtract, ...
                                    trialTout, startCount);
            end

        case '3'
            if strcmpi(lang, 'fr')
                Instruct = sprintf(['INSTRUCTIONS GENERALES :\n\nPour les prochaines 28 ', ...
                                    'minutes, vous devrez realiser (ordre indefini):\n\n\n', ...
                                    '1 tache d''effort mental\n2 periodes de repos\n\n\nAu ',...
                                    'moment venu, des instructions seront diffusees a l''ecran', ...
                                    ' pour\nvous guider tout au long de l''epreuve.\n\n\n\n', ...
                                    'Appuyez sur "SPACE" pour continuer >>> ']);
            elseif strcmpi(lang, 'en')
                Instruct = sprintf(['INSTRUCTIONS GENERALES :\n\nPour les prochaines 28 ', ...
                                    'minutes, vous devrez realiser (ordre indefini):\n\n\n', ...
                                    '1 tache d''effort mental\n2 periodes de repos\n\n\nAu ',...
                                    'moment venu, des instructions seront diffusees a l''ecran', ...
                                    ' pour\nvous guider tout au long de l''epreuve.\n\n\n\n', ...
                                    'Appuyez sur "SPACE" pour continuer >>> ']);
            end

        case '4'
            if strcmpi(lang, 'fr')
                Instruct = sprintf(['IMPORTANT :\n\nVous devrez rester calme au maximum et le ', ...
